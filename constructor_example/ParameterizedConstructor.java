package constructor_example;

public class ParameterizedConstructor {

    int var1;
    int var2;

    public ParameterizedConstructor() {
        this.var1 = var1;
        this.var2 = var2;
    }

    public void displayData(){

        System.out.println("variable1: "+this.var1);
        System.out.println("variable2: "+this.var2);

    }

}
